def dist(a, b):
    """"""
    return math.hypot(a[0] - b[0], a[1] - b[1])


def path_min_dist(p1, p2):
    """"""
    min_d, min_i = float('inf'), 0
    for i in range(len(p2)):
        d = dist(p1[-1], p2[i])
        if d < min_d:
            min_d, min_i = d, i
    # 
    p2_rot = p2[min_i:] + p2[:min_i]
    return min_d, p2_rot


def connect_paths(paths):
    """"""
    remaining = paths[1:]
    result = paths[0][:]
    ordered = [paths[0]]
    first_flag = True
    first_point = None
    while remaining:
        best_d, best_i, best_path = float('inf'), -1, None

        for i, p in enumerate(remaining):
            d, p2_rot = path_min_dist(result, p)
            if d < best_d:
                best_d, best_i, best_path = d, i, p2_rot
            if first_flag and best_path:
                 first_point = best_path
                 first_flag = False
        # 
        result += best_path
        ordered.append(best_path)
        remaining.pop(best_i)
    ordered.append(first_point)
    return result, ordered


